package base.domain.card;

/**
 * Represents the 13 face values of a standard playing card.
 * Declared in ascending order from lowest (TWO) to highest (ACE)
 * to utilize natural enum ranking.
 */
public enum Rank {
    TWO, THREE, FOUR, FIVE, SIX, SEVEN, EIGHT, NINE, TEN,
    JACK, QUEEN, KING, ACE

}